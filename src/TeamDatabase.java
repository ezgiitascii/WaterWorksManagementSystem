    private void statusAssignment_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statusAssignment_saveActionPerformed
        String Status = teamStatus.getText();
        int selectedrow = teamDatabase_Table.getSelectedRow();
        if(selectedrow == -1){
            if(model.getRowCount() == 0){
                assignmentMsg.setText("table is null");
            }
            else {
                assignmentMsg.setText("enter team information");
            }
        }
        else {
               int TT =(int)model.getValueAt(selectedrow,0);
               mainpage.statusAssignment_Save(TT,Status);
               teamDatabase_display();
               assignmentMsg.setText("successful");
        
        }
    }//GEN-LAST:event_statusAssignment_saveActionPerformed

