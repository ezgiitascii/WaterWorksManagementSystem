    public void teamDatabase_display() {
    
        model.setRowCount(0);
        ArrayList<TeamAccess> team_access = new ArrayList<TeamAccess>();
        team_access = mainpage.teamDatabase_bring();
        if(team_access != null){
            
            for(TeamAccess access :team_access ){
                Object[] access_add = { access.getTT(), access.getName(), access.getEmail(), access.getAddress(), access.getDescription(), access.getTeam(), access.getStatus()};
                model.addRow(access_add);
            
            }
        }
    
    
    }
